export async function fetchProducts(): Promise<CoupangProduct[]> {
  const listPath = `/v2/providers/seller_api/apis/api/v1/marketplace/seller-products?vendorId=${VENDOR_ID}&status=APPROVED&limit=100`
  const listRes = await coupangRequest<{ data: ProductListItem[] }>('GET', listPath)
  const list = listRes.data ?? []

  const results: CoupangProduct[] = []
  for (const p of list) {
    try {
      const detailPath = `/v2/providers/seller_api/apis/api/v1/marketplace/seller-products/${p.sellerProductId}`
      const detailRes = await coupangRequest<{ data: ProductDetail }>('GET', detailPath)
      const d = detailRes.data
      const firstItem = d.items?.[0]
      const salePrice =
        firstItem?.marketplaceItemData?.priceData?.salePrice ??
        firstItem?.rocketGrowthItemData?.priceData?.salePrice ?? 0

      const firstImage = (d as unknown as Record<string, unknown>).images as Record<string, unknown>[] | undefined
      const imageUrl = String(firstImage?.[0]?.cdnPath ?? firstImage?.[0]?.vendorPath ?? '')

      results.push({
        sellerProductId: String(d.sellerProductId),
        sellerProductName: d.sellerProductName,
        salePrice,
        statusName: d.statusName,
        imageUrl,
        items: (d.items ?? []).map(item => ({
          // prefer RG vendorItemId for matching with RG inventory API
          vendorItemId: String(
            item.rocketGrowthItemData?.vendorItemId ??
            item.marketplaceItemData?.vendorItemId ?? ''
          ),
          itemName: item.itemName ?? '',
        })),
      })
    } catch {
      // skip individual product if detail fetch fails
    }
  }
  return results
}
