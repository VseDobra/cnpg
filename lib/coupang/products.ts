export async function fetchProductForEdit(sellerProductId: string): Promise<ProductEditData> {
  const product = await fetchProductRaw(sellerProductId)

  const items = (product.items as Record<string, unknown>[] | undefined) ?? []
  const firstItem = items[0] as Record<string, unknown> | undefined
  let salePrice = 0, originalPrice = 0, maximumBuyForPerson = 0, unitCount = 1
  let outboundShippingTimeDay = 1, maximumBuyForPersonPeriod = 1
  let barcode = '', externalVendorSku = '', modelNo = ''
  let weight = 0, netWeight = 0, width = 0, length = 0, height = 0
  let fragile = false, distributionPeriod = 0
  if (firstItem) {
    const rgData = firstItem.rocketGrowthItemData as Record<string, unknown> | undefined
    const mpData = firstItem.marketplaceItemData as Record<string, unknown> | undefined
    const priceData = (rgData?.priceData ?? mpData?.priceData) as Record<string, unknown> | undefined
    salePrice = Number(priceData?.salePrice ?? 0)
    originalPrice = Number(priceData?.originalPrice ?? 0)
    maximumBuyForPerson = Number(firstItem.maximumBuyForPerson ?? 0)
    maximumBuyForPersonPeriod = Number(firstItem.maximumBuyForPersonPeriod ?? 1)
    unitCount = Number(firstItem.unitCount ?? 1)
    outboundShippingTimeDay = Number(firstItem.outboundShippingTimeDay ?? 1)
    if (rgData) {
      barcode = String(rgData.barcode ?? '')
      externalVendorSku = String(rgData.externalVendorSku ?? '')
      modelNo = String(rgData.modelNo ?? '')
      const sku = rgData.skuInfo as Record<string, unknown> | undefined
      if (sku) {
        weight = Number(sku.weight ?? 0)
        netWeight = Number(sku.netWeight ?? 0)
        width = Number(sku.width ?? 0)
        length = Number(sku.length ?? 0)
        height = Number(sku.height ?? 0)
        fragile = Boolean(sku.fragile ?? false)
        distributionPeriod = Number(sku.distributionPeriod ?? 0)
      }
    }
  }

  const rawImages = (product.images as Record<string, unknown>[] | undefined) ?? []
  const images = rawImages.map(img => ({
    order: Number(img.imageOrder ?? 0),
    type: String(img.imageType ?? 'DETAIL'),
    url: String(img.cdnPath ?? img.vendorPath ?? ''),
    isCdn: !!img.cdnPath,
  }))

  const contents = (product.contents as Record<string, unknown>[] | undefined) ?? []
  let description = ''
  outer: for (const c of contents) {
    for (const d of (c.contentDetails as Record<string, unknown>[] | undefined) ?? []) {
      if (d.detailType === 'TEXT') { description = String(d.content ?? ''); break outer }
    }
  }

  const rawTags = (product.searchTags as unknown[]) ?? []
  const searchTags = rawTags.map(t => String(t)).filter(Boolean)

  return {
    name: String(product.sellerProductName ?? ''),
    displayName: String(product.displayProductName ?? ''),
    brand: String(product.brand ?? ''),
    manufacturer: String(product.manufacture ?? ''),
    salePrice,
    originalPrice,
    saleStartedAt: String(product.saleStartedAt ?? ''),
    saleEndedAt: String(product.saleEndedAt ?? ''),
    searchTags,
    maximumBuyForPerson,
    maximumBuyForPersonPeriod,
    unitCount,
    outboundShippingTimeDay,
    barcode,
    externalVendorSku,
    modelNo,
    weight,
    netWeight,
    width,
    length,
    height,
    fragile,
    distributionPeriod,
    taxType: String(product.taxType ?? 'TAX'),
    adultOnly: String(product.adultOnly ?? 'EVERYONE'),
    parallelImported: String(product.parallelImported ?? 'NOT_PARALLEL_IMPORTED'),
    offerCondition: String(product.offerCondition ?? 'NEW'),
    images,
    description,
    items: items.map(item => {
      const rgData = item.rocketGrowthItemData as Record<string, unknown> | undefined
      const mpData = item.marketplaceItemData as Record<string, unknown> | undefined
      const vendorItemId = String(rgData?.vendorItemId ?? mpData?.vendorItemId ?? '')
      return { vendorItemId, itemName: String(item.itemName ?? '') }
    }),
  }
}

export async function updateProduct(sellerProductId: string, updates: Partial<ProductEditData> & { price?: number }): Promise<void> {
  const product = await fetchProductRaw(sellerProductId)

  if (updates.name !== undefined) product.sellerProductName = updates.name
  if (updates.displayName !== undefined) product.displayProductName = updates.displayName
  if (updates.brand !== undefined) product.brand = updates.brand
  if (updates.manufacturer !== undefined) product.manufacture = updates.manufacturer
  if (updates.saleStartedAt !== undefined) product.saleStartedAt = updates.saleStartedAt
  if (updates.saleEndedAt !== undefined) product.saleEndedAt = updates.saleEndedAt
  if (updates.searchTags !== undefined) product.searchTags = updates.searchTags
  if (updates.taxType !== undefined) product.taxType = updates.taxType
  if (updates.adultOnly !== undefined) product.adultOnly = updates.adultOnly
  if (updates.parallelImported !== undefined) product.parallelImported = updates.parallelImported

  const itemFields = ['price', 'originalPrice', 'salePrice', 'maximumBuyForPerson', 'maximumBuyForPersonPeriod',
    'unitCount', 'outboundShippingTimeDay', 'barcode', 'externalVendorSku', 'modelNo',
    'weight', 'netWeight', 'width', 'length', 'height', 'fragile', 'distributionPeriod']
  if (itemFields.some(f => (updates as Record<string, unknown>)[f] !== undefined)) {
    const items = (product.items as Record<string, unknown>[] | undefined) ?? []
    for (const item of items) {
      const it = item as Record<string, unknown>
      if (updates.maximumBuyForPerson !== undefined) it.maximumBuyForPerson = updates.maximumBuyForPerson
      if (updates.maximumBuyForPersonPeriod !== undefined) it.maximumBuyForPersonPeriod = updates.maximumBuyForPersonPeriod
      if (updates.unitCount !== undefined) it.unitCount = updates.unitCount
      if (updates.outboundShippingTimeDay !== undefined) it.outboundShippingTimeDay = updates.outboundShippingTimeDay
      for (const key of ['rocketGrowthItemData', 'marketplaceItemData'] as const) {
        const data = it[key] as Record<string, unknown> | undefined
        if (!data) continue
        if (data.priceData) {
          const pd = data.priceData as Record<string, unknown>
          if (updates.price !== undefined) pd.salePrice = updates.price
          if (updates.salePrice !== undefined) pd.salePrice = updates.salePrice
          if (updates.originalPrice !== undefined) pd.originalPrice = updates.originalPrice
        }
        if (key === 'rocketGrowthItemData') {
          if (updates.barcode !== undefined) data.barcode = updates.barcode
          if (updates.externalVendorSku !== undefined) data.externalVendorSku = updates.externalVendorSku
          if (updates.modelNo !== undefined) data.modelNo = updates.modelNo
          const skuFields = ['weight', 'netWeight', 'width', 'length', 'height', 'fragile', 'distributionPeriod'] as const
          if (skuFields.some(f => (updates as Record<string, unknown>)[f] !== undefined)) {
            const sku = (data.skuInfo ?? {}) as Record<string, unknown>
            if (updates.weight !== undefined) sku.weight = updates.weight
            if (updates.netWeight !== undefined) sku.netWeight = updates.netWeight
            if (updates.width !== undefined) sku.width = updates.width
            if (updates.length !== undefined) sku.length = updates.length
            if (updates.height !== undefined) sku.height = updates.height
            if (updates.fragile !== undefined) sku.fragile = updates.fragile
            if (updates.distributionPeriod !== undefined) sku.distributionPeriod = updates.distributionPeriod
            data.skuInfo = sku
          }
        }
      }
    }
  }

  if (updates.images !== undefined) {
    product.images = updates.images.map(img => ({
      imageOrder: img.order,
      imageType: img.type,
      ...(img.isCdn ? { cdnPath: img.url } : { vendorPath: img.url }),
    }))
  }

  if (updates.description !== undefined) {
    const contents = (product.contents as Record<string, unknown>[] | undefined) ?? []
    let found = false
    outer: for (const c of contents) {
      for (const d of (c.contentDetails as Record<string, unknown>[] | undefined) ?? []) {
        if (d.detailType === 'TEXT') { d.content = updates.description; found = true; break outer }
      }
    }
    if (!found) {
      contents.push({ contentsType: 'TEXT', contentDetails: [{ content: updates.description, detailType: 'TEXT' }] })
      product.contents = contents
    }
  }

  await coupangRequest('PUT', '/v2/providers/seller_api/apis/api/v1/marketplace/seller-products', product)
}

