export async function createProduct(data: CreateProductData): Promise<number> {
  const images = data.images.map(img => ({
    imageOrder: img.order,
    imageType: img.type,
    ...(img.isCdn ? { cdnPath: img.url } : { vendorPath: img.url }),
  }))

  const contents = data.description
    ? [{ contentsType: 'TEXT', contentDetails: [{ content: data.description, detailType: 'TEXT' }] }]
    : []

  const skuInfo = {
    fragile: data.fragile,
    weight: data.weight || undefined,
    netWeight: data.netWeight || undefined,
    width: data.width || undefined,
    length: data.length || undefined,
    height: data.height || undefined,
    quantityPerBox: 1,
    distributionPeriod: data.distributionPeriod,
    expiredAtManaged: data.distributionPeriod > 0,
  }

  const barcodeFields = data.emptyBarcode
    ? { emptyBarcode: true, emptyBarcodeReason: data.emptyBarcodeReason || 'AUTO_GENERATED' }
    : { barcode: data.barcode || undefined }

  const body = {
    sellerProductName: data.sellerProductName,
    displayCategoryCode: data.displayCategoryCode,
    vendorId: data.vendorId,
    saleStartedAt: data.saleStartedAt,
    saleEndedAt: data.saleEndedAt,
    displayProductName: data.displayProductName || undefined,
    brand: data.brand || undefined,
    generalProductName: data.generalProductName || undefined,
    manufacture: data.manufacture || undefined,
    marketplaceShippingAndReturnInfo: null,
    vendorUserId: data.vendorUserId,
    requested: data.requested,
    searchTags: data.searchTags.length > 0 ? data.searchTags : undefined,
    items: [{
      itemName: data.itemName,
      maximumBuyForPerson: data.maximumBuyForPerson,
      maximumBuyForPersonPeriod: data.maximumBuyForPersonPeriod,
      outboundShippingTimeDay: data.outboundShippingTimeDay,
      unitCount: data.unitCount,
      adultOnly: data.adultOnly,
      taxType: data.taxType,
      parallelImported: data.parallelImported,
      overseasPurchased: data.overseasPurchased,
      pccNeeded: data.pccNeeded,
      offerCondition: data.offerCondition,
      images,
      notices: data.notices.length > 0 ? data.notices : undefined,
      attributes: data.attributes.length > 0 ? data.attributes : undefined,
      contents,
      certifications: data.certifications.length > 0
        ? data.certifications.map(c => ({ certificationType: c.certificationType, certificationCode: c.certificationCode, certificationAttachments: null }))
        : [{ certificationType: 'NOT_REQUIRED', certificationCode: '', certificationAttachments: null }],
      rocketGrowthItemData: {
        ...barcodeFields,
        externalVendorSku: data.externalVendorSku || undefined,
        modelNo: data.modelNo || undefined,
        skuInfo,
        priceData: {
          originalPrice: data.originalPrice,
          salePrice: data.salePrice,
        },
      },
      marketplaceItemData: null,
    }],
    registrationType: 'RFM',
    rocketGrowthAdditionalInformation: {
      rfmInboundName: data.rfmInboundName || data.sellerProductName,
      legalAgreement: 'AGREE',
    },
  }

  const res = await coupangRequest<{ data: { data: number } }>('POST', '/v2/providers/seller_api/apis/api/v1/marketplace/seller-products', body)
  return res.data.data
}

